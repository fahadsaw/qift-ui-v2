// Saudi Arabia (المملكة العربية السعودية)
//
// Hierarchy: Country → Region (منطقة) → City/Governorate (مدينة/محافظة)
//          → District (حي).
//
// Coverage targets all 13 administrative regions, every regional
// capital, every governorate that contains a settlement of any
// material size, and a district list for each — using real
// neighbourhood names where they're publicly known and generic
// directional / center markers (وسط / شمال / جنوب / شرق / غرب)
// for very small towns where no formal district scheme exists. The
// generic markers are labelled clearly in the comment above each
// affected entry so future contributors can replace them with
// real names when they ship.
//
// Tier labels:
//   region       → addr.region        ("المنطقة")
//   city         → addr.city          ("المدينة")
//   governorate  → addr.governorate   ("المحافظة")    (optional)
//   district     → addr.district      ("الحي")
//
// The `governorate` tier is optional because the regional capital
// (Riyadh, Jeddah/Mecca, Dammam, etc.) sits directly under its
// region. For those cities the governorate slot stays blank and the
// catalog jumps from city → district. For sub-cities (Al-Khar,
// Diriyah, Yanbu, etc.) the governorate name is the same as the
// city — KSA's administrative geography uses "محافظة" both for the
// area governorate and for its principal city.

